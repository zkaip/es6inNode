var FLT_EXPONENT = 0b01111111100000000000000000000000; // 2139095040
var FLT_MANTISSA = 0B00000000011111111111111111111111; // 8388607

console.log(FLT_SIGNBIT)
console.log(FLT_EXPONENT)
console.log(FLT_MANTISSA)
// 2147483648
// 2139095040
// 8388607